// 3D-printable filament swatch generation. The swatch is a small card meant to
// be printed in the filament itself, carrying the same info as the printed
// labels (vendor, name, material, temperatures, id) plus a Spoolman QR code.
// The text/QR sit on a separate one-layer-thick top part, in black or white —
// whichever contrasts best with the filament color — so it can be printed with
// a filament change at the last layer or a multi-material setup.

